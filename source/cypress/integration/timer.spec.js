describe('Timer display', () => {
  before(cy.prepare);

  it('Counts the timer', () => {
    cy.reload();

    cy.clock()
    cy.get('#control').click()
    cy.tick(5000)
    cy.get('#counter').contains('24:55');
  });
});

describe('Timer control buttons', () => {
  before(cy.prepare);

  it('Shows the start button', () => {
    cy.get('#control').should('be.visible').contains('Start Pomo');
  })

  it('Hides the start button when clicked', () => {
    cy.get('#control').click();
    cy.get('#control').should('not.be.visible');
  });

  it('Starts the timer when space button pressed', () => {
    cy.reload()
      .get('body').type(' ') // Selecting the body allows us to type a space without having to focus an input
      .get('#control').should('not.be.visible');
  });

  it('Shows the start break button after 1 pomo', () => {
    cy.reload()
      .advanceTimer(1)
      .tick(POMO_TIME)
      .get('#transition-btn').should('be.visible').contains('Start Break');
  });

  it('Shows the start pomo button for pomo 2', () => {
    cy.reload()
      .advanceTimer(2)
      .tick(BREAK_TIME)
      .get('#transition-btn').should('be.visible').contains('Start Pomo');
  });

  it('Shows the start long break button', () => {
    cy.reload()
      .advanceTimer(7)
      .tick(POMO_TIME)
      .get('#transition-btn').should('be.visible').contains('Start Long Break');
  });

  it('Shows the long break skip button after 15 minutes', () => {
    cy.reload()
      .advanceTimer(8)
      .tick(14 * 60 * 1000)
      .get('#skip-long-break-btn').should('not.be.visible')
      .tick(60 * 1000)
      .get('#skip-long-break-btn').should('be.visible').contains('Skip');
  });
});

describe('Bubbles', () => {
  before(cy.prepare);

  it('Bubbles after first pomo', () => {
    cy.clock()
      .get('#control').click()
      .tick(POMO_TIME)
      .get('#dot1').should('have.class', 'dot-filled');
  });

  it('Does not bubble after a break', () => {
    cy.clock()
      .get('#transition-btn').click() // start next break
      .tick(BREAK_TIME)
      .get('#dot2').should('not.have.class', 'dot-filled');
  });

  it('Bubbles after second pomo', () => {
    cy.reload()
      .advanceTimer(4) // pomo1, break, pomo2, break
      .get('#dot2').should('have.class', 'dot-filled');
  });

  it('Fills all bubbles by the long break', () => {
    cy.reload()
      .advanceTimer(8);

    for (let i = 1; i <= 4; i++) {
      cy.get(`#dot${i}`).should('have.class', 'dot-filled');
    }
  });

  it('Resets all bubbles on wrap around to first pomo', () => {
    cy.reload()
      .advanceTimer(9);

    for (let i = 1; i <= 4; i++) {
      cy.get(`#dot${i}`).should('not.have.class', 'dot-filled');
    }
  })
});

describe('AutoBreak/AutoPomo', () => {
  before(cy.prepare);

  it('Enables AutoBreak and AutoPomo', () => {
    cy.get('.sidebarIconToggle').click({ force: true })
      .get('#auto-break-toggle').click({ force: true })
      .get('#auto-pomo-toggle').click({ force: true })
      .get('.sidebarIconToggle').click({ force: true });
  });

  it('Starts breaks and pomos automatically', () => {
    cy.clock()
      .get('#control').click()
      .tick(POMO_TIME)
      .get('#transition-btn').should('not.be.visible')
      .tick(5 * 1000)
      .get('#counter').contains('4:55')
      // Run through 3 breaks and pomos and just check that we make it to the long break without any button presses
      .tick((3 * POMO_TIME) + (3 * BREAK_TIME) - 5 * 1000)
      .get('#transition-btn').should('not.be.visible')
      .tick(5 * 1000)
      .get('#counter').contains('29:55');
  });
});
